package pkg4thqtritj;
public class DataTypes {
    public static void main(String[] args) {
        boolean truth = true;
        System.out.println("The boolean inside truth is: "+truth);
        char c1= 'B';
        System.out.println("The character inside c1 is:"+c1);
       //interger
       byte b1= 123;
       short s1= 12345;
       int il= 1234567;
       long l1= 12345678L;
        System.out.println("The byte inside b1 is:"+b1);
        System.out.println("The short inside s1 is:"+ s1);
        System.out.println("The int inside il is:"+il);
        System.out.println("The long inside l1 is:"+l1);
        //floating-point numbers
        float f1= 2.2F;
        double d1= 3.3; 
        System.out.println("The float inside f1 is:"+f1);
        System.out.println("The double inside d1 is:"+d1);
    }
}
